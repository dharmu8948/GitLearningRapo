class Typecasting
{
public static void main(String[] args)
{
	double d=130.56;
	int x=(int)d;
	byte b=(byte)d;
	short s=(short)d;
	long l=(long)d;
	float f=(float)d;
	System.out.println(x);
	System.out.println(b);
	System.out.println(s);
	System.out.println(l);
	System.out.println(f);
}
}